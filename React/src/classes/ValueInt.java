package classes;

public class ValueInt {
    int valueInt;

    public int getValueInt() {
        return valueInt;
    }

    public void setValueInt(int valueInt) {
        this.valueInt = valueInt;
    }

    @Override
    public String toString() {
        return "ValueInt{" +
                "valueInt=" + valueInt +
                '}';
    }
}
